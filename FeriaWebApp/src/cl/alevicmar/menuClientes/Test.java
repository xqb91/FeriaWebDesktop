    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            Coordinate coordenada = new Coordinate();
            coordenada.setValuesForAddress("Santiago Chile", "AIzaSyBlBpAuNX_TdrNjqY2Om6jPr14i01_dXf8");
            coordenada.setKey("AIzaSyBlBpAuNX_TdrNjqY2Om6jPr14i01_dXf8");
            coordenada.setLatitude(-33.4488897);
            coordenada.setLongitude(-70.6692655);
            Marker punto = new Marker(Colors.ColorRed, coordenada, Colors.SizeMid, 'p');
            ArrayList<Marker> listaPuntos = new ArrayList<Marker>();
            listaPuntos.add(punto);
            
            MarkersMap mapa = new MarkersMap("AIzaSyBlBpAuNX_TdrNjqY2Om6jPr14i01_dXf8", coordenada, 11, listaPuntos);
            mapa.setDimmension(new Dimension(500, 500));
            mapa.setMapType(MarkersMap.RoadMap);
            this.lblMapa.setIcon(mapa.getMapImage());
        }catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

